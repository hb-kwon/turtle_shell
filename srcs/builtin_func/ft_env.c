#include "minishell.h"

int	ft_env(char *line, char **en)
{
	int i;

	i = 0;
	while(en[i])
	{
		write(1, en[i], ft_strlen(en[i]));
		write(1,"\n",1);
		i++;
	}
	(void)line;
	return (0);
}
